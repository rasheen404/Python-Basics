month = int(input("Enter the month number 1 to 12 : "))
if month == 1 :
    print("JANUARY")
elif month == 2 :
    print("FEBRUARY")
elif month == 3 :
    print("MARCH")
elif month == 4 :
    print("APRIL")
elif month == 5 :
    print("MAY")
elif month == 6 :
    print("JUNE")
elif month == 7 :
    print("JULY")
elif month == 8 :
    print("AUGUST")    
elif month == 9 :
    print("SEPTEMBER")
elif month == 10 :
    print("OCTOBER")
elif month == 11 :
    print("NOVEMBER")
elif month == 12 :
    print("DECEMBER")
else :
    print("Invalid input\nPlease select between 1 to 12")